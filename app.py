# ==========================
# FUNCTION TO DRAW HEATMAP
# ==========================
def draw_heatmap(x, y, cmap, title):
    pitch = Pitch(
        pitch_type="statsbomb",
        pitch_color="#6BB36B",
        line_color="white"
    )

    fig, ax = pitch.draw(figsize=(7, 5))  # 👈 tamanho controlado

    pitch.kdeplot(
        x, y,
        ax=ax,
        cmap=cmap,
        shade=True,
        levels=100,
        alpha=0.7
    )

    pitch.scatter(
        x, y,
        ax=ax,
        color="black",
        s=10,
        alpha=0.5
    )

    ax.set_title(title, fontsize=13)
    return fig

